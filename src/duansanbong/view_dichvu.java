  private DichVu readFormDV() {
    int iddv = 0; // Bạn có thể thay đổi cách tạo iddv nếu cần, chẳng hạn nếu iddv là tự động tăng thì có thể để mặc định là 0
    String tendv = txttendv.getText().trim();
    double phidv = 0.0;
    String trangthai = null;

    // Kiểm tra Tên Dịch vụ
    if (tendv.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Tên Dịch vụ không được để trống", "Lỗi", JOptionPane.ERROR_MESSAGE);
        return null;
    }

    // Đọc và kiểm tra Phí Dịch vụ
    try {
        phidv = Double.parseDouble(txtphidv.getText().trim());
        if (phidv <= 0) {
            JOptionPane.showMessageDialog(this, "Phí Dịch vụ phải lớn hơn 0", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return null;
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Phí Dịch vụ không hợp lệ", "Lỗi", JOptionPane.ERROR_MESSAGE);
        return null;
    }

    // Đọc và kiểm tra Trạng thái
    trangthai = (String) cboTrangthai.getSelectedItem();
    if (trangthai == null || trangthai.trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Trạng thái không được để trống", "Lỗi", JOptionPane.ERROR_MESSAGE);
        return null;
    }

    // Tạo đối tượng DichVu và trả về
    DichVu dichVu = new DichVu(iddv, tendv, phidv, trangthai);
    return dichVu;
}



