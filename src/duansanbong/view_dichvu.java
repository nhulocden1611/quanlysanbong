    private void btnsuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsuaActionPerformed
      int rowIndex = TblBangDichVu2.getSelectedRow();
    if (rowIndex >= 0) {
        // Lấy ID dịch vụ từ hàng được chọn
        int idDichVu = (int) TblBangDichVu2.getValueAt(rowIndex, 0); 
        
        // Đọc dữ liệu từ form
        DichVu dichvu = readFormDV();
        if (dichvu != null) {
            // Sửa dịch vụ với ID đã chọn
            int result = dv.sua(idDichVu, dichvu);
            if (result > 0) {
                loadtabledv(dv.getDV());
                JOptionPane.showMessageDialog(this, "Sửa thành công");
            } else {
                JOptionPane.showMessageDialog(this, "Lỗi: Không thể sửa dịch vụ");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Không thể sửa dịch vụ. Vui lòng kiểm tra lại thông tin");
        }
    } else {
        JOptionPane.showMessageDialog(this, "Vui lòng chọn dịch vụ cần sửa");
    }
    }//GEN-LAST:event_btnsuaActionPerformed

