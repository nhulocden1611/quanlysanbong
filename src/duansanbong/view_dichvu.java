    private void updateTable() {
        // Tạo đối tượng service_dichvu để lấy danh sách dịch vụ
        service_dichvu serviceDV = new service_dichvu();
        ArrayList<DichVu> listDV = serviceDV.getDV();

        // Tạo mô hình bảng với dữ liệu mới
        DefaultTableModel model = (DefaultTableModel) TblBangDichVu2.getModel();
        model.setRowCount(0); // Xóa tất cả các hàng hiện tại

        for (DichVu dv : listDV) {
            model.addRow(new Object[]{dv.getIdDichVu(), dv.getTenDV(), dv.getPhiDV(), dv.getTrangThai()});
        }
    }

